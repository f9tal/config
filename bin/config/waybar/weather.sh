#!/bin/bash

weather_data=$(curl -s "wttr.in?format=%C,%t")
condition=$(echo "$weather_data" | cut -d ',' -f1 | sed 's/ //g')
temperature=$(echo "$weather_data" | cut -d ',' -f2 | sed 's/ //g')

case "$condition" in
  "Clear")
    icon="☀️"
    ;;
  "Sunny")
    icon="☀️"
    ;;
  "Partlycloudy")
    icon="⛅️"
    ;;
  "Cloudy")
    icon="☁️"
    ;;
  "Overcast")
    icon="☁️"
    ;;
  "Mist")
    icon="🌫️"
    ;;
  "Patchyrainpossible")
    icon="🌦️"
    ;;
  "Patchysnowpossible")
    icon="🌨️"
    ;;
  "Patchysleetpossible")
    icon="🌨️"
    ;;
  "Patchyfreezingdrizzlepossible")
    icon="🌨️"
    ;;
  "Thunderyoutbreakspossible")
    icon="⛈️"
    ;;
  "Blowingsnow")
    icon="🌨️"
    ;;
  "Blizzard")
    icon="🌨️"
    ;;
  "Fog")
    icon="🌫️"
    ;;
  "Freezingfog")
    icon="🌫️"
    ;;
  "Drizzle")
    icon="🌦️"
    ;;
  "Freezingdrizzle")
    icon="🌦️"
    ;;
  "Lightrain")
    icon="🌦️"
    ;;
  "Moderaterain")
    icon="🌧️"
    ;;
  "Heavyrain")
    icon="🌧️"
    ;;
  "Lightsnow")
    icon="🌨️"
    ;;
  "Moderatesnow")
    icon="🌨️"
    ;;
  "Heavysnow")
    icon="🌨️"
    ;;
  "Icepellets")
    icon="🌨️"
    ;;
  "Lightrainshower")
    icon="🌦️"
    ;;
  "Moderateorheavyrainshower")
    icon="🌧️"
    ;;
  "Torrentialrainshower")
    icon="🌧️"
    ;;
  "Lightsleetshowers")
    icon="🌨️"
    ;;
  "Moderateorheavysleetshowers")
    icon="🌨️"
    ;;
  "Lightsnowshowers")
    icon="🌨️"
    ;;
  "Moderateorheavysnowshowers")
    icon="🌨️"
    ;;
  "Lightshowersoficepellets")
    icon="🌨️"
    ;;
  "Moderateorheavyshowersoficepellets")
    icon="🌨️"
    ;;
  "Patchylightrainwiththunder")
    icon="⛈️"
    ;;
  "Moderateorheavyrainwiththunder")
    icon="⛈️"
    ;;
  "Patchylightsnowwiththunder")
    icon="⛈️"
    ;;
  "Moderateorheavysnowwiththunder")
    icon="⛈️"
    ;;
  *)
    icon=""
    ;;
esac

echo "$icon $temperature"